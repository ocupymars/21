int		ft_is_sort(int *tab, int length, int (*f)(int, int))
{
	int		i;
	int		s;

	s = 1;
	i = 0;
	while (i < length - 1 && s)
	{
		if ((*f)(tab[i], tab[i + 1]) < 0)
			s = 0;
		i++;
	}
	if (s != 1)
	{
		s = 1;
		i = 0;
		while (i < length - 1)
		{
			if ((*f)(tab[i], tab[i + 1]) > 0)
				return (0);
			i++;
		}
	}
	return (1);
}
