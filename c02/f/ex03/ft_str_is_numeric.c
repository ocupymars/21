int		ft_str_is_numeric(char *str)
{
	unsigned	int		i;

	i = 0;
	while (str[1])
	{
		if (!(str[i] >= '0' && str[i] <= '9'))
		{
			return (0);
		}
		i++;
	}
	return (1);
}
